package com.ca.arcserve.hypervisor.data;

public class SubHypervisorInfo {
	
	private String name;
	
	private String dcName;
	
	private String dcDisplayName;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDcName() {
		return dcName;
	}

	public void setDcName(String dcName) {
		this.dcName = dcName;
	}

	public String getDcDisplayName() {
		return dcDisplayName;
	}

	public void setDcDisplayName(String dcDisplayName) {
		this.dcDisplayName = dcDisplayName;
	}
	
}
